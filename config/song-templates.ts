export function lyricExcerpt(lyrics: string, maxLength = 150) {
  const text = lyrics.replace(/\[[^\]]+\]/g, "").replace(/\s+/g, " ").trim();

  if (text.length <= maxLength) {
    return text;
  }

  return `${text.slice(0, maxLength - 1).trim()}...`;
}
